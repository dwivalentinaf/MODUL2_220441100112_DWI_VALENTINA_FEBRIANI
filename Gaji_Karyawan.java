    private void bHitungBrutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHitungBrutoActionPerformed
        // TODO add your handling code here:
        try {
        double jumlahJamKerja = Double.parseDouble(txtJumlahJamKerja.getText());
        double tarifJam = Double.parseDouble(txtTarifJam.getText());

        
        if (jumlahJamKerja <= 0 || tarifJam <= 0) {
            JOptionPane.showMessageDialog(this, "Masukkan jumlah jam kerja dan tarif per jam yang valid.");
            return; 
        }

        double gajiBruto;

        if (jumlahJamKerja <= 40) {
            gajiBruto = jumlahJamKerja * tarifJam;
        } else {
            double jamNormal = 40;
            double jamLembur = jumlahJamKerja - jamNormal;
            gajiBruto = (jamNormal * tarifJam) + (jamLembur * tarifJam * LEMBUR_RATE);
        }

        txtGajiBruto.setText(String.valueOf(gajiBruto));
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan jumlah jam kerja dan tarif per jam dengan benar.");
    }
    }//GEN-LAST:event_bHitungBrutoActionPerformed

