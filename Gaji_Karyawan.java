    private void bGajiBersihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGajiBersihActionPerformed
        // TODO add your handling code here:
         try {
            double gajiBruto = Double.parseDouble(txtGajiBruto.getText());
            double pajak = gajiBruto * PAJAK_PENGHASILAN;
            double gajiBersih = gajiBruto - pajak;

            txtGajiBersih.setText(String.valueOf(gajiBersih));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Hitung gaji bruto terlebih dahulu.");
        }
    }//GEN-LAST:event_bGajiBersihActionPerformed

